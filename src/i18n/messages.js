export function t(key, params = {}, language = getSystemLanguage()) {
  const nextLanguage = normalizeSystemLanguage(language, "zh");
  const template =
    MESSAGES[nextLanguage]?.[key] ??
    MESSAGES.zh[key] ??
    key;

  return Object.entries(params).reduce((result, [paramKey, paramValue]) => {
    return result.replaceAll(`{${paramKey}}`, String(paramValue));
  }, template);
}
